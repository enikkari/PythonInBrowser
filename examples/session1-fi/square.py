# Piirretään neliö

import turtle

##### INFO #####
#
# Tehtävänäsi on piirtää neliö ruudulle. Kerrataan muutama
# kilpikonnan ymmärtämä komento.

# Alla oleva komento tuo kilpikonnan ruudulle ja antaa sille
# nimen t.
t = turtle.Turtle()

# Tämä rivi kertoo että haluamme kilpikonnan muotoisen
# kynän. Ilman tätä riviä muoto olisi kolmio.
t.shape("turtle")

# Liikutetaan kilpikonnaa eteenpäin 100 askelta.
t.forward(100)

# Käännetään kilpikonnaa 90 astetta vasemmalle.
t.left(90)

# Kuljetaan taas suoraan.
t.forward(50)

