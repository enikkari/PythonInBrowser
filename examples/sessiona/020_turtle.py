##
## Kilpparia voi käskyttää liikkumaan ruudulla.
## Alla kilpparia käsketään liikkumaan 150 pikseliä suoraan,
## kääntymään vasemmalle 90 astetta
## ja liikkumaan taas suoraan 100 pikseliä.

taylor.forward(150)
taylor.left(90)
taylor.forward(100)

