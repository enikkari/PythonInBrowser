# Importing turtle and assigning it to a variable ’t’.
import turtle
t = turtle.Turtle()

# By now you might remember that Turtle is a way we can draw on the canvas.
# Below we are moving Turtle forward 100 pixels,
# turning it left 90 degrees and moving it forward again 100 pixels
# then turning right 90 degrees, going forward 20 pixels,
# turning left 135 pixels and finally going forward 100 pixels.

t.forward(100)
t.left(90)
t.forward(100)
t.right(90)
t.forward(20)
t.left(135)
t.forward(100)

